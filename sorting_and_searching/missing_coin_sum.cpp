void solve() {
    int n;
    cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++)
        cin >> v[i];
    sort(v.begin(), v.end());

    int prefix_sum = 0;
    for (int i = 0; i < n; i++) {
        if (prefix_sum < v[i]-1) {
            break;
        }
        prefix_sum += v[i];
    }
    cout << prefix_sum + 1 << '\n';
}

signed main () {
    ios::sync_with_stdio(0); 
    cin.tie(0); 
    cout.tie(0);

    int t = 1;
    // cin >> t;
    while (t--) solve();

    return 0;
}